import 'dart:convert';

import 'package:build/build.dart';

/// Generates `_reference.g.dart` and `_stubs.g.dart` from a
/// `*.love_api.json` snapshot produced by `tool/update_love_api_snapshot.dart`.
class LoveApiBuilder implements Builder {
  @override
  final Map<String, List<String>> buildExtensions = const {
    '.love_api.json': ['_reference.g.dart', '_stubs.g.dart'],
  };

  @override
  Future<void> build(BuildStep buildStep) async {
    final inputId = buildStep.inputId;
    final content = await buildStep.readAsString(inputId);
    final json = jsonDecode(content) as Map<String, Object?>;

    final version = json['version'] as String;
    final sourceCommit = json['sourceCommit'] as String;
    final modules = (json['modules'] as List<Object?>)
        .cast<Map<String, Object?>>();
    final symbols = (json['symbols'] as List<Object?>)
        .cast<Map<String, Object?>>();
    final types = (json['types'] as List<Object?>).cast<Map<String, Object?>>();
    final enums = (json['enums'] as List<Object?>).cast<Map<String, Object?>>();

    final refId = AssetId(
      inputId.package,
      inputId.path.replaceFirst('.love_api.json', '_reference.g.dart'),
    );
    await buildStep.writeAsString(
      refId,
      _generateReference(version, sourceCommit, modules, symbols, types, enums),
    );

    final stubsId = AssetId(
      inputId.package,
      inputId.path.replaceFirst('.love_api.json', '_stubs.g.dart'),
    );
    await buildStep.writeAsString(stubsId, _generateStubs(symbols));
  }

  // ---------------------------------------------------------------------------
  // Reference file
  // ---------------------------------------------------------------------------

  String _generateReference(
    String version,
    String sourceCommit,
    List<Map<String, Object?>> modules,
    List<Map<String, Object?>> symbols,
    List<Map<String, Object?>> types,
    List<Map<String, Object?>> enums,
  ) {
    final buf = StringBuffer();

    buf.writeln('// GENERATED CODE - DO NOT MODIFY BY HAND.');
    buf.writeln(
      '// Generated by `dart run build_runner build` via LoveApiBuilder.',
    );
    buf.writeln();
    buf.writeln('library;');
    buf.writeln();
    buf.writeln("import 'package:lualike/library_builder.dart';");
    buf.writeln();
    buf.writeln("import '../love_api_overrides.dart';");
    buf.writeln("import '../love_api_support.dart';");
    buf.writeln("import 'love_api_stubs.g.dart';");
    buf.writeln();

    buf.writeln('const String loveApiVersion = ${jsonEncode(version)};');
    buf.writeln(
      'const String loveApiSourceCommit = ${jsonEncode(sourceCommit)};',
    );
    buf.writeln();

    // Modules
    buf.writeln('const List<LoveModuleDoc> loveApiModules = <LoveModuleDoc>[');
    for (final m in modules) {
      buf.writeln('  LoveModuleDoc(');
      buf.writeln('    symbol: ${jsonEncode(m['symbol'])},');
      buf.writeln('    name: ${jsonEncode(m['name'])},');
      buf.writeln('    description: ${jsonEncode(m['description'])},');
      buf.writeln('    wikiPath: ${jsonEncode(m['wikiPath'])},');
      buf.writeln('  ),');
    }
    buf.writeln('];');
    buf.writeln();

    // Symbols
    buf.writeln(
      'const List<LoveApiSymbolDoc> loveApiSymbols = <LoveApiSymbolDoc>[',
    );
    for (final s in symbols) {
      buf.writeln('  LoveApiSymbolDoc(');
      buf.writeln('    symbol: ${jsonEncode(s['symbol'])},');
      buf.writeln('    module: ${jsonEncode(s['module'])},');
      buf.writeln('    name: ${jsonEncode(s['name'])},');
      buf.writeln('    kind: ${jsonEncode(s['kind'])},');
      buf.writeln('    description: ${jsonEncode(s['description'])},');
      if (s['container'] != null) {
        buf.writeln('    container: ${jsonEncode(s['container'])},');
      }
      final variants = (s['variants'] as List<Object?>)
          .cast<Map<String, Object?>>();
      buf.writeln('    variants: <LoveApiVariantDoc>[');
      for (final v in variants) {
        buf.writeln('      LoveApiVariantDoc(');
        final args = (v['arguments'] as List<Object?>)
            .cast<Map<String, Object?>>();
        buf.writeln('        arguments: <LoveApiArgumentDoc>[');
        for (final a in args) {
          buf.writeln('          LoveApiArgumentDoc(');
          buf.writeln('            name: ${jsonEncode(a['name'])},');
          buf.writeln('            type: ${jsonEncode(a['type'])},');
          buf.writeln(
            '            description: ${jsonEncode(a['description'])},',
          );
          if (a['defaultValue'] != null) {
            buf.writeln(
              '            defaultValue: ${jsonEncode(a['defaultValue'])},',
            );
          }
          buf.writeln('          ),');
        }
        buf.writeln('        ],');
        final rets = (v['returns'] as List<Object?>)
            .cast<Map<String, Object?>>();
        buf.writeln('        returns: <LoveApiReturnDoc>[');
        for (final r in rets) {
          buf.writeln('          LoveApiReturnDoc(');
          buf.writeln('            name: ${jsonEncode(r['name'])},');
          buf.writeln('            type: ${jsonEncode(r['type'])},');
          buf.writeln(
            '            description: ${jsonEncode(r['description'])},',
          );
          buf.writeln('          ),');
        }
        buf.writeln('        ],');
        buf.writeln('      ),');
      }
      buf.writeln('    ],');
      buf.writeln('    wikiPath: ${jsonEncode(s['wikiPath'])},');
      buf.writeln('  ),');
    }
    buf.writeln('];');
    buf.writeln();

    // Types
    buf.writeln('const List<LoveTypeDoc> loveApiTypes = <LoveTypeDoc>[');
    for (final t in types) {
      buf.writeln('  LoveTypeDoc(');
      buf.writeln('    symbol: ${jsonEncode(t['symbol'])},');
      buf.writeln('    module: ${jsonEncode(t['module'])},');
      buf.writeln('    name: ${jsonEncode(t['name'])},');
      buf.writeln('    description: ${jsonEncode(t['description'])},');
      final supertypes = (t['supertypes'] as List<Object?>).cast<String>();
      buf.writeln('    supertypes: <String>[');
      for (final st in supertypes) {
        buf.writeln('      ${jsonEncode(st)},');
      }
      buf.writeln('    ],');
      final methodSymbols = (t['methodSymbols'] as List<Object?>)
          .cast<String>();
      buf.writeln('    methodSymbols: <String>[');
      for (final ms in methodSymbols) {
        buf.writeln('      ${jsonEncode(ms)},');
      }
      buf.writeln('    ],');
      buf.writeln('    wikiPath: ${jsonEncode(t['wikiPath'])},');
      buf.writeln('  ),');
    }
    buf.writeln('];');
    buf.writeln();

    // Enums
    buf.writeln('const List<LoveEnumDoc> loveApiEnums = <LoveEnumDoc>[');
    for (final e in enums) {
      buf.writeln('  LoveEnumDoc(');
      buf.writeln('    symbol: ${jsonEncode(e['symbol'])},');
      buf.writeln('    module: ${jsonEncode(e['module'])},');
      buf.writeln('    name: ${jsonEncode(e['name'])},');
      buf.writeln('    description: ${jsonEncode(e['description'])},');
      final constants = (e['constants'] as List<Object?>)
          .cast<Map<String, Object?>>();
      buf.writeln('    constants: <LoveEnumConstantDoc>[');
      for (final c in constants) {
        buf.writeln('      LoveEnumConstantDoc(');
        buf.writeln('        name: ${jsonEncode(c['name'])},');
        buf.writeln('        description: ${jsonEncode(c['description'])},');
        buf.writeln('      ),');
      }
      buf.writeln('    ],');
      buf.writeln('    wikiPath: ${jsonEncode(e['wikiPath'])},');
      buf.writeln('  ),');
    }
    buf.writeln('];');

    return buf.toString();
  }

  // ---------------------------------------------------------------------------
  // Stubs file
  // ---------------------------------------------------------------------------

  String _generateStubs(List<Map<String, Object?>> symbols) {
    final buf = StringBuffer();

    buf.writeln('// GENERATED CODE - DO NOT MODIFY BY HAND.');
    buf.writeln(
      '// Generated by `dart run build_runner build` via LoveApiBuilder.',
    );
    buf.writeln();
    buf.writeln('library;');
    buf.writeln();
    buf.writeln("import '../love_api_support.dart';");
    buf.writeln();

    for (final s in symbols) {
      final symbol = s['symbol'] as String;
      final description = s['description'] as String;
      final wikiPath = s['wikiPath'] as String;
      final container = s['container'] as String?;
      final variants = (s['variants'] as List<Object?>)
          .cast<Map<String, Object?>>();

      // Doc comment
      buf.writeln('/// $description');
      buf.writeln('///');
      buf.writeln('/// LOVE symbol: `$symbol`.');
      buf.writeln('/// Wiki: `$wikiPath`.');
      if (container != null) {
        buf.writeln('/// Container: `$container`.');
      }
      buf.writeln('/// Variants:');
      for (final v in variants) {
        buf.writeln('/// - `${_variantSignature(symbol, v)}`');
      }

      // Function declaration
      final funcName = _stubName(symbol);
      buf.writeln('Object? $funcName(List<Object?> args) =>');
      buf.writeln('    loveApiUnimplemented(${jsonEncode(symbol)});');
      buf.writeln();
    }

    return buf.toString();
  }

  // ---------------------------------------------------------------------------
  // Helpers
  // ---------------------------------------------------------------------------

  /// Converts a LOVE symbol like `love.audio.getEffect` or
  /// `RecordingDevice:getBitDepth` into a stub function name like
  /// `stubLoveAudioGetEffect` or `stubRecordingDeviceGetBitDepth`.
  String _stubName(String symbol) {
    final parts = symbol.split(RegExp(r'[.:]'));
    final pascal = parts.map(_capitalize).join();
    return 'stub$pascal';
  }

  String _capitalize(String s) =>
      s.isEmpty ? s : s[0].toUpperCase() + s.substring(1);

  /// Builds a one-line variant signature like `love.audio.newSource(type) -> source`.
  String _variantSignature(String symbol, Map<String, Object?> variant) {
    final args = (variant['arguments'] as List<Object?>)
        .cast<Map<String, Object?>>();
    final rets = (variant['returns'] as List<Object?>)
        .cast<Map<String, Object?>>();

    final argPart = args.map((a) => a['name'] as String).join(', ');
    final retPart = rets.map((r) => r['name'] as String).join(', ');

    final sig = StringBuffer('$symbol($argPart)');
    if (retPart.isNotEmpty) {
      sig.write(' -> $retPart');
    }
    return sig.toString();
  }
}
