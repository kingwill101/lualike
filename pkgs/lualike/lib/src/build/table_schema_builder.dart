import 'package:analyzer/dart/constant/value.dart';
import 'package:analyzer/dart/element/element.dart';
import 'package:analyzer/dart/element/type.dart';
import 'package:build/build.dart';

/// Builder that generates [TableDoc] constants from `@TableSchema`-annotated
/// Dart classes.
class TableSchemaBuilder implements Builder {
  @override
  final Map<String, List<String>> buildExtensions = const {
    '.dart': ['.table_schema.g.dart'],
  };

  @override
  Future<void> build(BuildStep buildStep) async {
    final inputId = buildStep.inputId;
    if (!_isSchemaFile(inputId.path)) return;

    final resolver = buildStep.resolver;
    final library = await resolver.libraryFor(inputId);

    final classes = library.classes
        .where((cls) => cls.name != null && cls.name!.isNotEmpty && _hasAnnotation(cls, 'TableSchema'))
        .toList();
    if (classes.isEmpty) return;

    final output = AssetId(inputId.package,
        inputId.path.replaceFirst('.dart', '.table_schema.g.dart'));
    await buildStep.writeAsString(output, _generate(classes));
  }

  bool _isSchemaFile(String path) {
    return path.endsWith('_schema.dart') ||
        path.endsWith('_plugin_info.dart') ||
        path.contains('table') && path.endsWith('.dart');
  }

  bool _hasAnnotation(ClassElement cls, String annotationName) {
    for (final annot in cls.metadata.annotations) {
      final element = annot.element;
      if (element is ConstructorElement) {
        final type = element.enclosingElement;
        if (type is ClassElement && type.name == annotationName) {
          return true;
        }
      }
    }
    return false;
  }

  DartObject? _annotationValue(Element element, String annotationName) {
    for (final annot in element.metadata.annotations) {
      final el = annot.element;
      if (el is ConstructorElement) {
        final type = el.enclosingElement;
        if (type is ClassElement && type.name == annotationName) {
          return annot.computeConstantValue();
        }
      }
    }
    return null;
  }

  String _generate(List<ClassElement> classes) {
    final buf = StringBuffer();
    buf.writeln('// GENERATED CODE — DO NOT MODIFY BY HAND.');
    buf.writeln('// Generated by TableSchemaBuilder.');
    buf.writeln();
    buf.writeln(
        "import 'package:lualike/library_builder.dart' show FieldDoc, TableDoc;");
    buf.writeln();

    for (final cls in classes) {
      final annot = _annotationValue(cls, 'TableSchema');
      final docName = _stringField(annot, 'name') ?? cls.name!;
      final description = _stringField(annot, 'description') ?? '';
      final varName = _lowerCamel(cls.name!);

      buf.writeln('final $varName = TableDoc(');
      buf.writeln('  name: ${_literal(docName)},');
      buf.writeln('  description: ${_literal(description)},');
      buf.writeln('  fields: [');

      for (final field in cls.fields) {
        final fieldName = field.name;
        if (fieldName == null || fieldName.isEmpty) continue;

        final fieldAnnot = _annotationValue(field, 'SchemaField');
        final fieldType = _stringField(fieldAnnot, 'type') ??
            _inferType(field.type);
        final fieldDesc = _stringField(fieldAnnot, 'description') ?? '';
        final required = _boolField(fieldAnnot, 'required') ?? false;

        buf.writeln('    FieldDoc(');
        buf.writeln('      key: ${_literal(fieldName)},');
        buf.writeln('      type: ${_literal(fieldType)},');
        buf.writeln('      description: ${_literal(fieldDesc)},');
        buf.writeln('      required: ${_boolLiteral(required)},');
        _writeField(buf, 'defaultValue', fieldAnnot, 'defaultValue');
        _writeStringField(buf, 'group', fieldAnnot, 'group');
        _writeStringField(buf, 'dependsOn', fieldAnnot, 'dependsOn');
        _writeNumField(buf, 'min', fieldAnnot, 'min');
        _writeNumField(buf, 'max', fieldAnnot, 'max');
        _writeNumField(buf, 'step', fieldAnnot, 'step');
        _writeListField(buf, 'choices', fieldAnnot, 'choices');
        buf.writeln('    ),');
      }

      buf.writeln('  ],');
      buf.writeln(');');
      buf.writeln();
    }

    return buf.toString();
  }

  // ----------------------------------------------------------------
  // Constant-value helpers
  // ----------------------------------------------------------------

  String? _stringField(DartObject? obj, String fieldName) {
    if (obj == null) return null;
    final field = obj.getField(fieldName);
    if (field == null || field.isNull) return null;
    return field.toStringValue();
  }

  bool? _boolField(DartObject? obj, String fieldName) {
    if (obj == null) return null;
    final field = obj.getField(fieldName);
    if (field == null || field.isNull) return null;
    return field.toBoolValue();
  }

  double? _numField(DartObject? obj, String fieldName) {
    if (obj == null) return null;
    final field = obj.getField(fieldName);
    if (field == null || field.isNull) return null;
    final intVal = field.toIntValue();
    if (intVal != null) return intVal.toDouble();
    return field.toDoubleValue();
  }

  List<DartObject>? _listField(DartObject? obj, String fieldName) {
    if (obj == null) return null;
    final field = obj.getField(fieldName);
    if (field == null || field.isNull) return null;
    return field.toListValue();
  }

  void _writeField(
      StringBuffer buf, String paramName, DartObject? obj, String fieldName) {
    if (obj == null) return;
    final field = obj.getField(fieldName);
    if (field == null || field.isNull) return;
    final strVal = field.toStringValue();
    if (strVal != null) {
      buf.writeln('      $paramName: ${_literal(strVal)},');
      return;
    }
    final intVal = field.toIntValue();
    if (intVal != null) {
      buf.writeln('      $paramName: $_escapeInt(intVal),');
      return;
    }
    final boolVal = field.toBoolValue();
    if (boolVal != null) {
      buf.writeln('      $paramName: ${_boolLiteral(boolVal)},');
      return;
    }
    final doubleVal = field.toDoubleValue();
    if (doubleVal != null) {
      buf.writeln('      $paramName: $doubleVal,');
    }
  }

  void _writeStringField(
      StringBuffer buf, String paramName, DartObject? obj, String fieldName) {
    final value = _stringField(obj, fieldName);
    if (value == null || value.isEmpty) return;
    buf.writeln('      $paramName: ${_literal(value)},');
  }

  void _writeNumField(
      StringBuffer buf, String paramName, DartObject? obj, String fieldName) {
    final value = _numField(obj, fieldName);
    if (value == null) return;
    // Write as int if no fractional part, otherwise as double
    if (value == value.truncateToDouble()) {
      buf.writeln('      $paramName: ${value.toInt()},');
    } else {
      buf.writeln('      $paramName: $value,');
    }
  }

  void _writeListField(
      StringBuffer buf, String paramName, DartObject? obj, String fieldName) {
    final list = _listField(obj, fieldName);
    if (list == null || list.isEmpty) return;
    final items =
        list.map((e) => _literal(e.toStringValue() ?? '')).join(', ');
    buf.writeln('      $paramName: [$items],');
  }

  String _inferType(DartType type) {
    final name = type.getDisplayString();
    return switch (name) {
      'String' => 'string',
      'int' => 'integer',
      'double' || 'num' => 'number',
      'bool' => 'boolean',
      'List' || 'List<' || 'List<String>' || 'List<Object>' => 'array',
      'Map' || 'Map<' => 'table',
      _ => name,
    };
  }

  String _literal(String value) =>
      "'${value.replaceAll("'", "\\'")}'";

  String _boolLiteral(bool value) => value ? 'true' : 'false';

  String _escapeInt(int value) => value.toString();

  String _lowerCamel(String name) =>
      name.isEmpty ? name : name[0].toLowerCase() + name.substring(1);
}
