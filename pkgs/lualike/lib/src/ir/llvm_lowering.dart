  // ---------------------------------------------------------------------------
  // Test / TestSet
  // ---------------------------------------------------------------------------

  /// `TEST rA, k` — skip next instruction if truthiness(rA) == k.
  void _emitTest(
    int a,
    bool k,
    Map<int, LualikeIrSsaValue> currentValues,
  ) {
    final aVal = currentValues[a];
    if (aVal == null) {
      _emitAbort('test on undefined register r$a');
      return;
    }

    final aType = typeAnalysis.typeOf(aVal);
    switch (aType) {
      case LualikeIrSsaType.number:
        // Numbers are always truthy.
        if (k) {
          _emitAbort('test on always-truthy number with k=1 (skip always)');
        }
        // No-op: fall through.
        return;
      case LualikeIrSsaType.boolean:
      case LualikeIrSsaType.nil_:
        // Emit a truthiness flag without branching — the `jmp` instruction
        // uses this to emit br.
        final aRef = _ssaRef(aVal);
        final cond = _nextUnnamed();
        _writeln('  $cond = icmp ne i8 $aRef, 0');
        _testCondition = (cond: cond, k: k);
        return;
      case LualikeIrSsaType.unknown:
        _emitAbort('test on unknown-typed value');
    }
  }

  /// `TESTSET rA, rB, k` — if truthiness(rB) == k, skip next instruction;
  /// otherwise set rA = rB.
  void _emitTestSet(
    int a,
    int b,
    bool k,
    Map<int, LualikeIrSsaValue> currentValues,
  ) {
    final bVal = currentValues[b];
    if (bVal == null) {
      _emitAbort('testSet on undefined register r$b');
      return;
    }
    final aVal = _findDefinedValue(a, currentValues);
    if (aVal == null) return;

    final bType = typeAnalysis.typeOf(bVal);
    switch (bType) {
      case LualikeIrSsaType.number:
        if (k) {
          return;
        }
        _emitMove(a, b, currentValues);
        return;
      case LualikeIrSsaType.boolean:
      case LualikeIrSsaType.nil_:
        final bRef = _ssaRef(bVal);
        final cond = _nextUnnamed();
        _writeln('  $cond = icmp ne i8 $bRef, 0');
        _testCondition = (cond: cond, k: k);
        return;
      case LualikeIrSsaType.unknown:
        _emitAbort('testSet on unknown-typed value');
    }
  }

  // ---------------------------------------------------------------------------
  // Control flow
  // ---------------------------------------------------------------------------

  void _emitJmp(int offset, Map<int, LualikeIrSsaValue> currentValues) {
    final targetPc = _currentPc + 1 + offset;
    final block = ssaFunction.blocks[_currentSsaBlock];

    if (_testCondition != null) {
      // Conditional jump: test+jmp pair.
      final c = _testCondition!;
      final condVar = _nextUnnamed();
      // TEST k=0 skips JMP when falsy; k=1 skips when truthy.
      // Invert: JMP target is taken when the test skips.
      _writeln('  $condVar = xor i1 ${c.cond}, true');

      final jmpTarget = _blockIndexContainingPc(
        ssaFunction.blocks, targetPc,
      );
      if (jmpTarget == null) {
        _emitAbort('conditional jmp to unknown target');
        return;
      }
      // Fall-through is the other successor block.
      final fallthrough = block.block.successors
          .firstWhere((s) => s != jmpTarget, orElse: () => -1);
      if (fallthrough < 0) {
        _writeln('  br label %${_blockLabel(jmpTarget)}');
        _terminated = true;
      } else {
        _writeln(
          '  br i1 $condVar, label %${_blockLabel(jmpTarget)}, label %${_blockLabel(fallthrough)}',
        );
        _terminated = true;
      }
      _testCondition = null;
    } else {
      // Unconditional jump.
      final targetIndex = _blockIndexContainingPc(
        ssaFunction.blocks, targetPc,
      );
      if (targetIndex != null) {
        _writeln('  br label %${_blockLabel(targetIndex)}');
        _terminated = true;
      } else {
        _emitAbort('jmp to unknown target');
      }
    }
  }

  void _emitForPrep(
    int a,
    int sBx,
    Map<int, LualikeIrSsaValue> currentValues,
  ) {
    // FORPREP defines new SSA versions for r[a]..r[a+3].
    // Emit copy instructions so these appear as LLVM values.
    for (var i = 0; i < 4; i++) {
      final reg = a + i;
      final ssaVal = _findDefinedValue(reg, currentValues);
      if (ssaVal == null) continue;
      // Version 0 values are never defined as LLVM instructions.
      // Emit with a zero constant when the source is uninitialized.
      final srcVal = currentValues[reg];
      if (srcVal != null && srcVal.version == 0) {
        _writeln('  ${_ssaRef(ssaVal)} = fadd double 0.0, 0.0');
      } else {
        final srcRef = _doubleRef(reg, currentValues);
        _writeln('  ${_ssaRef(ssaVal)} = fadd double $srcRef, 0.0');
      }
      currentValues[reg] = ssaVal;
    }

    final varRef = _doubleRef(a, currentValues);
    final limitRef = _doubleRef(a + 1, currentValues);
    final stepRef = _doubleRef(a + 2, currentValues);

    // Check if step > 0 and compare var with limit based on step direction.
    final t0 = _nextUnnamed();
    _writeln('  $t0 = fcmp ogt double $stepRef, 0.0');
    final t2 = _nextUnnamed();
    final t3 = _nextUnnamed();
    _writeln('  $t2 = fcmp ole double $varRef, $limitRef');
    _writeln('  $t3 = fcmp oge double $varRef, $limitRef');
    final cond = _nextUnnamed();
    _writeln('  $cond = select i1 $t0, i1 $t2, i1 $t3');

    final targetBlock = _blockIndexContainingPc(
      ssaFunction.blocks, _currentPc + 1 + sBx,
    );
    final skipBlock = _blockIndexContainingPc(
      ssaFunction.blocks, _currentPc + 1,
    );

    if (targetBlock != null && skipBlock != null) {
      if (targetBlock == skipBlock) {
        _writeln('  br label %${_blockLabel(targetBlock)}');
      } else {
        _writeln(
          '  br i1 $cond, label %${_blockLabel(targetBlock)}, label %${_blockLabel(skipBlock)}',
        );
      }
      _terminated = true;
    } else {
      _emitAbort('forPrep: unknown branch targets');
    }
  }

  void _emitForLoop(
    int a,
    int sBx,
    Map<int, LualikeIrSsaValue> currentValues,
  ) {
    // FORLOOP defines r[a]..r[a+3].  Only r[a] is actually updated;
    // the rest are pass-through copies to satisfy the SSA versions.
    final varRef = _doubleRef(a, currentValues);
    final stepRef = _doubleRef(a + 2, currentValues);
    final limitRef = _doubleRef(a + 1, currentValues);

    // Update the loop variable: r[a] += r[a+2] (step).
    final newVar = _findDefinedValue(a, currentValues);
    if (newVar == null) {
      _emitAbort('forLoop: no SSA value for loop variable');
      return;
    }
    _writeln('  ${_ssaRef(newVar)} = fadd double $varRef, $stepRef');
    currentValues[a] = newVar;

    // Pass-through copies for r[a+1], r[a+2], r[a+3].
    for (var i = 1; i < 4; i++) {
      final reg = a + i;
      final ssaVal = _findDefinedValue(reg, currentValues);
      if (ssaVal == null) continue;
      final srcRef = _doubleRef(reg, currentValues);
      _writeln('  ${_ssaRef(ssaVal)} = fadd double $srcRef, 0.0');
      currentValues[reg] = ssaVal;
    }

    final t0 = _nextUnnamed();
    _writeln('  $t0 = fcmp ogt double $stepRef, 0.0');
    final t1 = _nextUnnamed();
    final t2 = _nextUnnamed();
    _writeln('  $t1 = fcmp ole double ${_ssaRef(newVar)}, $limitRef');
    _writeln('  $t2 = fcmp oge double ${_ssaRef(newVar)}, $limitRef');
    final cond = _nextUnnamed();
    _writeln('  $cond = select i1 $t0, i1 $t1, i1 $t2');

    final targetBlock = _blockIndexContainingPc(
      ssaFunction.blocks, _currentPc + 1 + sBx,
    );
    final exitBlock = _blockIndexContainingPc(
      ssaFunction.blocks, _currentPc + 1,
    );

    if (targetBlock != null && exitBlock != null) {
      if (targetBlock == exitBlock) {
        _writeln('  br label %${_blockLabel(targetBlock)}');
      } else {
        _writeln(
          '  br i1 $cond, label %${_blockLabel(targetBlock)}, label %${_blockLabel(exitBlock)}',
        );
      }
      _terminated = true;
    } else {
      _emitAbort('forLoop: unknown branch targets');
    }
  }

  // ---------------------------------------------------------------------------
  // Returns
  // ---------------------------------------------------------------------------

  void _emitReturn1(int a, Map<int, LualikeIrSsaValue> currentValues) {
    final aVal = currentValues[a];
    if (aVal == null) {
      _writeln('  ret double 0.0');
      _terminated = true;
      return;
    }
    final aType = typeAnalysis.typeOf(aVal);
    if (aType == LualikeIrSsaType.number) {
      final aRef = _doubleRef(a, currentValues);
      _writeln('  ret double $aRef');
      _terminated = true;
    } else {
      _writeln('  ret double 0.0');
      _terminated = true;
    }
  }

  void _emitReturn0() {
    _writeln('  ret void');
    _terminated = true;
  }

  void _emitRet(int a, int b, Map<int, LualikeIrSsaValue> currentValues) {
    if (b == 1) {
      _writeln('  ret void');
      _terminated = true;
    } else if (b == 2) {
      _emitReturn1(a, currentValues);
    } else {
      _emitAbort('multi-return not supported in compiled code');
    }
  }

  // ---------------------------------------------------------------------------
  // Abort
  // ---------------------------------------------------------------------------

  void _emitAbort(String message) {
    final msgId = _nextUnnamed();
    _writeln('  $msgId = alloca i8, i64 ${message.length + 1}');
    for (var i = 0; i < message.length; i++) {
      final gep = _nextUnnamed();
      _writeln('  $gep = getelementptr i8, i8* $msgId, i64 $i');
      _writeln(
        '  store i8 ${message.codeUnitAt(i)}, i8* $gep',
      );
    }
    final gepEnd = _nextUnnamed();
    _writeln('  $gepEnd = getelementptr i8, i8* $msgId, i64 ${message.length}');
    _writeln('  store i8 0, i8* $gepEnd');
    _writeln('  call void @lualike_abort(i8* $msgId)');
    _writeln('  unreachable');
    _terminated = true;
    _aborted = true;
  }

  /// Set to true when an abort has been emitted; subsequent instructions in
  /// the same block become dead code and must not produce output.
  bool _aborted = false;
  bool _terminated = false;

  /// When non-null, a condition computed by TEST/TESTSET that the next JMP
  /// should use for a conditional branch.
  ({String cond, bool k})? _testCondition;

  // ---------------------------------------------------------------------------
  // Helpers
  // ---------------------------------------------------------------------------

  // _currentPc is set each instruction in _emitBlock

  /// Finds the SSA value defined for register [reg] at instruction [pc] in
  /// the current block, or falls back to the entry value if not explicitly
  /// defined in this block.
  int _currentPc = 0;

  LualikeIrSsaValue? _findDefinedValue(
    int reg,
    Map<int, LualikeIrSsaValue> currentValues,
  ) {
    // Walk the current block's defined values to find one whose pc matches.
    // This gives us the correct SSA version for instructions that redefine
    // a register (e.g. LOADI then ADDI to the same register).
    final block = ssaFunction.blocks[_currentSsaBlock];
    for (final value in block.definedValues) {
      if (value.register == reg && value.definingPc == _currentPc) {
        return value;
      }
    }
    return currentValues[reg];
  }

  int _currentSsaBlock = 0;

  /// Returns the LLVM IR type string for an SSA value.
  String? _llvmType(LualikeIrSsaValue value) {
    return switch (typeAnalysis.typeOf(value)) {
      LualikeIrSsaType.nil_ || LualikeIrSsaType.boolean => 'i8',
      LualikeIrSsaType.number => 'double',
      LualikeIrSsaType.unknown => null,
    };
  }

  /// Returns the LLVM IR reference string for [value].
  String _llvmRef(
    LualikeIrSsaValue value,
    Map<int, LualikeIrSsaValue> currentValues,
  ) {
    final ssType = _llvmType(value);
    if (ssType == null) return 'double 0.0';
    return '$ssType ${_ssaRef(value)}';
  }

  /// Returns a value reference for register [reg] (without type prefix).
  String _doubleRef(int reg, Map<int, LualikeIrSsaValue> currentValues) {
    final value = currentValues[reg];
    if (value == null) return '0.0';
    return _ssaRef(value);
  }

  /// Returns the SSA reference string (e.g. `%r5_v12`) for an SSA value.
  String _ssaRef(LualikeIrSsaValue value) {
    return '%r${value.register}_v${value.version}';
  }

  String _blockLabel(int index) => 'b$index';

  String _nextBlockLabel() {
    final label = 'b${_blockCounter}';
    _blockCounter++;
    return label;
  }

  String _nextUnnamed() => '%t${_unnamedCounter++}';

  int? _blockIndexContainingPc(
    List<LualikeIrSsaBlock> blocks,
    int pc,
  ) {
    for (final block in blocks) {
      if (block.block.startPc <= pc && pc < block.block.endPc) {
        return block.block.index;
      }
    }
    return null;
  }

  String _doubleLiteral(double value) {
    // Use LLVM hex float format via raw bit representation.
    if (value.isNaN) return '0x7FF8000000000000';
    if (value.isInfinite) {
      return value.isNegative
          ? '0xFFF0000000000000'
          : '0x7FF0000000000000';
    }
    // Dart's toString produces a round-trippable short representation.
    return value.toString();
  }

  int _signExtend(int value, int bits) {
    final shift = 32 - bits;
    return (value << shift) >> shift;
  }

  double? _constantDoubleValue(int index) {
    if (index < 0 || index >= prototype.constants.length) return null;
    return switch (prototype.constants[index]) {
      NumberConstant(value: final v) => v,
      IntegerConstant(value: final v) => v.toDouble(),
      _ => null,
    };
  }

  void _writeln([String line = '']) {
    _buffer.writeln(line);
  }
}
